export default {
  SET_NEWS(state, news) {
    return state.news = news;
  },
  SET_JOBS(state, jobs) {
    return state.jobs = jobs;
  },
  SET_ASK(state, ask) {
    return state.ask = ask;
  },
  SET_USER(state, user) {
    return state.user = user;
  },
  SET_ITEM(state, item) {
    return state.item = item;
  }
}